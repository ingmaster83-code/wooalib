def main():
    src = Path(__file__).parent.parent / "_data" / "libraries.json"
    libraries = json.loads(src.read_text(encoding='utf-8'))

    matched = 0
    missing = set()
    for lib in libraries:
        key = (lib['sido'], lib['sigungu'])
        stdg = STDG_MAP.get(key, '')
        lib['stdg_cd'] = stdg
        if stdg:
            matched += 1
        else:
            missing.add(key)

    src.write_text(json.dumps(libraries, ensure_ascii=False, indent=2), encoding='utf-8')
    print(f"매핑 완료: {matched}/{len(libraries)}건")
    if missing:
        print(f"\n미매핑 ({len(missing)}개):")
        for s, sg in sorted(missing):
            print(f"  ({repr(s)}, {repr(sg)})")

if __name__ == '__main__':
    main()
